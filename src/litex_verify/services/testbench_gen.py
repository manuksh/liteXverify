"""Testbench generation service."""

from __future__ import annotations

from pathlib import Path
from textwrap import dedent

import aiofiles

from litex_verify.generators.cocotb.bfm import CocotbBFMGenerator
from litex_verify.generators.cocotb.testbench import CocotbTestGenerator
from litex_verify.generators.uvm.agent import InterfaceSignal, InterfaceSpec, UVMAgentGenerator
from litex_verify.generators.uvm.env import UVMEnvGenerator
from litex_verify.generators.uvm.ral import UVMRALGenerator
from litex_verify.generators.uvm.sequences import UVMSequenceGenerator
from litex_verify.generators.uvm.tests import UVMTestGenerator
from litex_verify.utils.templates import create_template_environment


async def _write(path: Path, content: str) -> None:
    """Write *content* to *path* via aiofiles (no raw Path.write_text)."""
    async with aiofiles.open(path, "w", encoding="utf-8") as fh:
        await fh.write(content)


class TestbenchGenerator:
    def __init__(self, workspace: Path | None = None) -> None:
        self.workspace = (workspace or Path.cwd()).resolve()
        self.env = create_template_environment(self.workspace)

    async def generate_uvm(self, output_dir: Path, top_module: str) -> list[Path]:
        output_dir.mkdir(parents=True, exist_ok=True)
        agent_name = f"{top_module}_if"
        spec = InterfaceSpec(
            name=agent_name,
            protocol="wishbone",
            signals=[
                InterfaceSignal(name="clk", direction="input", width=1),
                InterfaceSignal(name="rst", direction="input", width=1),
            ],
        )
        generated: list[Path] = []
        generated.extend(await UVMAgentGenerator(self.env).generate(spec, output_dir))
        generated.append(await UVMEnvGenerator(self.env).generate(output_dir, agent_name))
        generated.append(await UVMRALGenerator().generate(output_dir, top_module))
        generated.append(await UVMSequenceGenerator(self.env).generate(output_dir, agent_name))
        generated.append(await UVMTestGenerator(self.env).generate(output_dir, agent_name))
        top_tb = output_dir / "top_tb.sv"
        top_tb_content = dedent(f"""\
            // Auto-generated by LiteXVerify
            `timescale 1ns/1ps
            module top_tb;
              logic clk = 0;
              logic rst = 1;
              always #5 clk = ~clk;
              {top_module} dut(.clk(clk), .rst(rst));
              initial begin
                #20 rst = 0;
                #1000;
                $finish;
              end
            endmodule
        """)
        await _write(top_tb, top_tb_content)
        generated.append(top_tb)
        return generated

    async def generate_cocotb(self, output_dir: Path, top_module: str) -> list[Path]:
        output_dir.mkdir(parents=True, exist_ok=True)
        generated: list[Path] = []
        generated.append(await CocotbTestGenerator(self.env).generate(output_dir, top_module))
        generated.append(await CocotbBFMGenerator(self.env).generate(output_dir, top_module))
        # Rename output to conventional name so Jinja BFM is named 'tb_utils.py'
        bfm_path = output_dir / f"bfm_{top_module}.py"
        tb_utils = output_dir / "tb_utils.py"
        if bfm_path.exists() and not tb_utils.exists():
            bfm_path.rename(tb_utils)
            generated = [p if p != bfm_path else tb_utils for p in generated]
        sim_dir = output_dir.parent / "sim" / "questa"
        sim_dir.mkdir(parents=True, exist_ok=True)
        makefile = output_dir / "Makefile"
        makefile_content = dedent(f"""\
            # Auto-generated by LiteXVerify
            SIM        ?= icarus
            TOPLEVEL   = {top_module}
            MODULE     = test_{top_module}
            VERILOG_SOURCES += $(shell find ../build -name '*.v' 2>/dev/null)
            include $(shell cocotb-config --makefiles)/Makefile.sim
        """)
        await _write(makefile, makefile_content)
        generated.append(makefile)
        return generated
